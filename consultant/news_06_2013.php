      <ul><div class="data">24.06.2013</div><b>• Новое в Путеводителях КонсультантПлюс - практическое пособие по квартальной бухотчетности бюджетных организаций</b><br />
Новое "Практическое пособие по квартальной бухгалтерской отчетности бюджетных и автономных учреждений" поможет подготовить отчетность в соответствии с требованиями законодательства. Оно содержит общие требования к отчетности; порядок и образцы заполнения форм с контрольными соотношениями показателей. Уникальный раздел пособия - "Взаимоувязка показателей форм отчетности" - позволяет проверить, правильно ли сформирована отчетность. По ссылкам из пособия можно перейти в нормативно-правовые акты (законы, указания, приказы и т.д.) и разъясняющие письма Минфина РФ и Федерального казначейства. Пособие подготовлено на основе приказа Минфина России от 25.03.2011 N 33н "Об утверждении Инструкции о порядке составления, представления годовой, квартальной бухгалтерской отчетности государственных (муниципальных) бюджетных и автономных учреждений" и адресовано финансовым специалистам государственных (муниципальных) бюджетных и автономных учреждений. Новое пособие включено в "Путеводитель по бюджетному учету и налогам" системы КонсультантПлюс.<br /></ul>
