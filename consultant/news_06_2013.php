      <ul><div class="data">06.06.2013</div><b>• Изменение размера арендной платы - разъяснения ВАС РФ в КонсультантПлюс.</b><br />
Согласно разъяснениям, данным в п. 21 Постановления Пленума ВАС РФ от 17.11.2011 N 73, норма п. 3 ст. 614 ГК РФ о возможности изменения арендной платы чаще раза в год является диспозитивной. Это означает, что стороны вправе неоднократно в течение года изменять размер арендных платежей путем заключения соответствующего соглашения. Вместе с тем, если арендодателю предоставлено право одностороннего изменения размера арендной платы, он может менять ее размер не чаще одного раза в год. Подробнее об этом читайте в "Путеводителе по договорной работе" в разделе 7.1.1 "Изменение размера арендной платы по соглашению сторон".<br /></ul>
