function loggi_up(){
	$('#err_up').text('').hide();
	if ($('#authLogin').val() && $('#authPassword').val()){
		var queryString = $('#logform').serialize();
		var jsurl = "/system/login.php?"+queryString+'&rnd='+Math.random(); 
		$.getJSON(jsurl, function(data){
			if (data.result == "fail"){
				$('#err_up').text('Такого логина/пароля не зарегистрировано!').show();
				return false;
			}else if (data.result == "status"){
				switch (data.status){
					case '0':
						$('#err_up').text('Ваш ник зарегистрирован. В течение 5-7 минут его проверит модератор и вы получите возможность оставлять сообщения на Infox.ru.').show();
						break;
					case '2':
						$('#err_up').text('Аккаунт заблокирован!').show(); 
						break;
					case '3': 
						$('#right_login_error').text('Уважаемый пользователь! К сожалению, ваш ник не соответствует правилам регистрации, принятым на Infox.ru для удобства и корректности общения, а также для идентификации пользователей. Просим вас сменить ник в личном кабинете. Надеемся на ваше понимание и будем рады увидеть вас среди наших комментаторов после смены ника.').show(); 
						break;
					case '5':
						$('#right_login_error').text('Аккаунт не активирован! Дождитесь письма с активацией аккаунта и перейдите по ссылке!').show(); 
						break;
				}
				return false;
			}else if (data.result == "ok"){
				if (typeof data.kod != 'undefined' && $('#authMemPass').attr('checked')){
					$.cookie('inxkod', data.kod, {path: "/", expires: 3650, domain: '.infox.ru'});
